/**
 * @Author:DLzZ2013
 * @Description:
 * @Date:Create in 16:32 2019/12/25
 * @Modified By:
 */
@SpringBootApplication
@EnableEurekaServer
public class EurekaApplication {
    public static void main(String[] args) {
        SpringApplication.run(EurekaApplication.class);
    }
}
